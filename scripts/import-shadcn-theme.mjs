#!/usr/bin/env node
import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';

const target = path.resolve('src/lib/themes/active.ts');

function usage() {
	console.error('Usage: bun run theme:import <theme-url|registry-json-url|theme-id|file>');
	console.error('Example: bun run theme:import https://tweakcn.com/themes/cmlm03etv000204lh15608kec');
	console.error('Example: bun run theme:import https://tweakcn.com/r/themes/modern-minimal.json');
	process.exit(1);
}

function normalizeSource(source) {
	const malformedHttpsId = /^https:([a-zA-Z0-9_-]+)$/.exec(source);
	if (malformedHttpsId) {
		return {
			readable: `https://tweakcn.com/r/themes/${malformedHttpsId[1]}`,
			comment: `https://tweakcn.com/themes/${malformedHttpsId[1]}`
		};
	}

	if (/^https?:\/\//.test(source)) {
		const url = new URL(source);
		const themeMatch = /^\/themes\/([^/]+)\/?$/.exec(url.pathname);

		if (url.hostname === 'tweakcn.com' && themeMatch) {
			return {
				readable: `https://tweakcn.com/r/themes/${themeMatch[1]}`,
				comment: `https://tweakcn.com/themes/${themeMatch[1]}`
			};
		}

		return { readable: source, comment: source };
	}

	if (/^[a-zA-Z0-9_-]+(?:\.json)?$/.test(source)) {
		const id = source.replace(/\.json$/, '');
		return {
			readable: `https://tweakcn.com/r/themes/${id}`,
			comment: `https://tweakcn.com/themes/${id}`
		};
	}

	return { readable: path.resolve(source), comment: source };
}

async function readSource(source) {
	if (/^https?:\/\//.test(source)) {
		try {
			const response = await fetch(source, { signal: AbortSignal.timeout(30_000) });
			if (!response.ok) {
				throw new Error(`${source} returned ${response.status} ${response.statusText}`);
			}
			return response.text();
		} catch (error) {
			if (error instanceof Error) {
				throw new Error(`Could not read ${source}: ${error.message}`);
			}
			throw error;
		}
	}

	return readFile(path.resolve(source), 'utf8');
}

function assertTheme(theme) {
	if (!theme || typeof theme !== 'object') throw new Error('Theme must be a JSON object.');
	if (!theme.name || typeof theme.name !== 'string') throw new Error('Theme is missing a string name.');
	if (!theme.cssVars || typeof theme.cssVars !== 'object') throw new Error('Theme is missing cssVars.');
	if (!theme.cssVars.light || typeof theme.cssVars.light !== 'object') {
		throw new Error('Theme is missing cssVars.light.');
	}
	if (!theme.cssVars.dark || typeof theme.cssVars.dark !== 'object') {
		throw new Error('Theme is missing cssVars.dark.');
	}
}

const source = process.argv[2];
if (!source) usage();

try {
	const resolved = normalizeSource(source);
	const raw = await readSource(resolved.readable);
	const theme = JSON.parse(raw);
	assertTheme(theme);

	const contents = `import type { ShadcnRegistryTheme } from '$lib/shadcn-theme';

// Generated by scripts/import-shadcn-theme.mjs from:
// ${resolved.comment}
export const activeTheme = ${JSON.stringify(theme, null, '\t')} satisfies ShadcnRegistryTheme;
`;

	await writeFile(target, contents);
	console.log(`Imported ${theme.name} into ${target}`);
} catch (error) {
	console.error(error instanceof Error ? error.message : String(error));
	process.exit(1);
}
